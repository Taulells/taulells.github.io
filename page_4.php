<p>
   <h3> Per què Taulells.org?</h3>
<p>Cada comerç local té situat a l'entrada un taulell on atén al client de la millor manera possible. Per aquesta raó vam triar el nom de Taulells, ja que és un objecte que engloba a tots els comerços locals.</p>

<h3>Que és Taulells.org ?</h3>
<p>Taulells.org és una eina digital pels comerços locals (inicialment de Molins de Rei) que dóna a conèixer els seus productes d'una forma senzilla i no abusiva. Aquesta aplicació va ser creada i posada a prova com a producte final d'un Treball de Recerca.</p>

<h3>Qui ha creat Taulells.org?</h3>
<p>Taulells.org l'ha creat un alumne de l'IES Bernat el Ferrer apassionat per la programació i les mates. Actualment està cursant 2n de Batxillerat i aquest és el seu producte final a la pregunta que en un principi es va qüestionar;</p>
<p>Pot el comerç local, un comerç nascut abans de l'era digital, subsistir avui dia i fer front als comerços electrònics com Amazon o AliExpress mitjançant la modernització dels seus trets característics? </p>

<h3>Puc jo contribuir en aquest projecte?</h3>
<p>Inicialment aquest projecte ha sigut desenvolupat per al Treball de Recerca (TR), quan el TR i la seva exposició finalitzi també es finalitzarà el servei de hosting de Taulells.org . Arran les peticions de continuïtat i el gran treball realitzat el creador del projecte ha deixat com a llegat tots els codis que conformen aquesta eina digital i es posa a disposició de contribuir i mantenir l'esperit de Taulells.org a Github. https://github.com/Taulells </p>
<p>Per consegüent el creador d'aquesta aplicació digital la deixa com a Projecte Open Source, on tothom podrà contribuir a millorar el comerç local mitjançant les noves tecnologies.</p>

<h3>Per què comprar al comerç local?</h3>
<p>Seguidament exposem 7 raons per comprar al comerç local de la teva vila;</p>
<ul>
<li>  Sempre són a prop teu</li>
<li>  Generen ocupació</li>
<li>  Redueixen la contaminació</li>
<li>  Li donen vida al teu barri</li>
<li>  Milloren l'economia</li>
<li>  Tenen el millor servei d'atenció al client</li>
<li>  Especialització. </li>
 </ul>
