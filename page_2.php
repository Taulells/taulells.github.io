<p>
    <h3>English</h3>
In this project we will develop a digital tool for the local commerce in the town of Molins de Rei, with the aim of breaking the unfair competition currently imposed by large e-commerce companies. A tool that will emphasize the characteristic features of local trade, such as personal service, immediacy or the ethical values that represent a product manufactured or produced in the area.
An applied R&D project where first of all we will investigate the past of these local shops and their adversities. We will then analyze the current situation through surveys in 28 shops in the town and then develop all parts of the digital tool using 5 programming languages. Finally, the digital application will be put into operation in the town of Molins de Rei and we will draw the final conclusions.
</p>


<p> 
<h3>Català</h3>
En aquest treball desenvoluparem una eina digital per al comerç local del poble  Molins de Rei, amb la voluntat de trencar la competència deslleial que imposen actualment les grans empreses del comerç electrònic. Una eina que emfatitzarà els trets característics del comerç de proximitat, com el servei personal, la immediatesa o els valors ètics que representen un producte fabricat o produït a la zona. 
Un treball R+D aplicat on primer de tot investigarem el passat d’aquests comerços i les seves adversitats. Després, analitzarem la situació actual mitjançant enquestes a 28 comerços de la vila i posteriorment desenvoluparem totes les parts de l’eina digital utilitzant 5 llenguatges de programació. Finalment, l’aplicació digital serà posada en funcionament a la vila Molins de Rei i n’extreurem les conclusions finals.
 
 </p>
 <p> 

<h3>Castellano</h3>
En este trabajo desarrollaremos una herramienta digital para el comercio local del pueblo Molins de Rei, con la voluntad de romper la competencia desleal que imponen actualmente las grandes empresas del comercio electrónico. Una herramienta que enfatizará los rasgos característicos del comercio de proximidad, como el servicio personal, la inmediatez o los valores éticos que representan un producto fabricado o producido en la zona.
Un trabajo I+D aplicado donde primero investigaremos el pasado de estos comercios y sus adversidades. Después, analizaremos la situación actual mediante encuestas en 28 comercios de la villa y posteriormente desarrollaremos todas las partes de la herramienta digital utilizando 5 lenguajes de programación. Finalmente, la aplicación digital será puesta en funcionamiento en la villa Molins de Rei y extraeremos las conclusiones finales.
 </p> 
