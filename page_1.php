<p> Pot el comerç local, un comerç nascut abans de l’era digital, subsistir avui dia i fer front als comerços electrònics com Amazon o AliExpress mitjançant la modernització dels seus trets característics?
En aquest treball desenvoluparem una eina digital amb la voluntat de trencar la competència deslleial que imposen les grans empreses de l'e-commerce actualment. Una eina que emfatitza els trets característics del comerç local, com la proximitat, servei personal o els valors ètics que representen un producte fabricat o produït a la zona. 
</p>
<p>  
Aquesta idea va sorgir arran les adversitats dels comerços de Molins de Rei durant la pandèmia de la COVID-19, on tots els comerços no essencials van haver de tancar. El tancament de les tendes va afectar astronòmicament a les economies de les persones que es dedicaven al comerç. 
La pandèmia ens ha tornat a demostrar el proverbi del ric i el pobre, “els rics es fan més rics, i els pobres més pobres”, els ecommerce principals han triplicat les seves vendes mentrestant el comerç local ha desaparegut a tot estrop. Tot i això, la pandèmia també ens ha ensenyat que l’única manera de mantenir i protegir una cosa que estimem és a partir de la recerca, innovació i tecnologia. Aquí l’exemple predominant són les vacunes contra la COVID, creades en un temps molt reduït gràcies als esforços de molts científics. 
</p>
<p>  
En aquest treball farem una cosa similar, utilitzarem els coneixements sobre les noves tecnologies de programació i cercarem les particularitats del comerç local per mantenir i protegir el teixit comercial de pobles i ciutats. En aquest treball no devaluarem ni criticarem les accions preses pels ecommerce, sinó que aprendrem sobre elles i intentarem trobar aspectes que els ecommerce no tenen, per poder-los aprofitar e innovar el comerç local d’avui dia. 
 </p> 
